const readline = require("readline");
const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout,
});

const solution = () => {
  const N = input();
  let arr = [];
  for (let i = 0; i < N; i++) {
    let temp = input();
    if (arr.includes(temp)) continue;
    arr.push(temp);
  }
  arr.sort().sort((a, b) => a.length - b.length);
  console.log(arr.join("\n"));
};

